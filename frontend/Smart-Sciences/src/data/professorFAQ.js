// ── Keyword matching engine ──────────────────────────────────────

/**
 * Find a matching FAQ response for user input.
 * @param {string} input - user message (lowercase recommended)
 * @param {'physics'|'chemistry'} subject
 * @returns {string} - Professor's answer
 */
export function matchProfessorResponse(input, subject) {
  const lower = input.toLowerCase()
  const faq = subject === 'physics' ? PHYSICS_FAQ : CHEMISTRY_FAQ

  const match = faq.find(entry =>
    entry.keywords.some(kw => lower.includes(kw.toLowerCase()))
  )

  if (match) return match.answer

  // Return a random fallback
  return FALLBACK_RESPONSES[Math.floor(Math.random() * FALLBACK_RESPONSES.length)]
}
